import { inject, Injectable, Signal } from '@angular/core';
import { PostgrestSingleResponse, SupabaseClient } from '@supabase/supabase-js';
import { Database } from '../util/schema';
import { BetModel, OmitAutoGeneratedColumns } from '../util/supabase-types';
import {
  realtimeUpdatesFromTable,
  Table,
  Function,
  BetStatus,
} from '../util/supabase-helpers';
import { map, Observable, shareReplay, combineLatest } from 'rxjs';
import { GameStateService } from './game-state.service';
import { PlayerService } from './player.service';
import { toSignal } from '@angular/core/rxjs-interop';
import { whenNotNull } from '../util/rxjs-helpers';

@Injectable({
  providedIn: 'root',
})
export class BetService {
  private readonly supabase: SupabaseClient<Database> = inject(SupabaseClient);
  private readonly gameStateService = inject(GameStateService);
  private readonly playerService = inject(PlayerService);

  readonly bets$: Observable<BetModel[] | null> =
    // get user player id
    this.playerService.userPlayer$.pipe(
      whenNotNull((player) =>
        // get both requester bets and opponent bets
        combineLatest({
          requesterBets: realtimeUpdatesFromTable(
            this.supabase,
            Table.Bet,
            `requester_player_id=eq.${player.player_id}`,
          ),
          opponentBets: realtimeUpdatesFromTable(
            this.supabase,
            Table.Bet,
            `opponent_player_id=eq.${player.player_id}`,
          ),
        }).pipe(
          map(({ requesterBets, opponentBets }) =>
            requesterBets
              // concatenate the 2 together
              .concat(opponentBets)
              // sort by date
              .sort(
                (a, b) =>
                  new Date(a.created_at).getTime() -
                  new Date(b.created_at).getTime(),
              ),
          ),
        ),
      ),
      shareReplay(1),
    );

  readonly bets: Signal<BetModel[] | null | undefined> = toSignal(this.bets$);

  readonly openBets$: Observable<BetModel[] | null> =
    // get both active and pending bets (should be almost entirely from the current session)
    combineLatest({
      activeBets: realtimeUpdatesFromTable(
        this.supabase,
        Table.Bet,
        `status=eq.${BetStatus.Active}`,
      ),
      pendingBets: realtimeUpdatesFromTable(
        this.supabase,
        Table.Bet,
        `status=eq.${BetStatus.PendingAcceptance}`,
      ),
      sessionId: this.gameStateService.sessionId$,
    }).pipe(
      map(({ activeBets, pendingBets, sessionId }) =>
        activeBets
          // sort by date
          .sort(
            (a, b) =>
              new Date(a.created_at).getTime() -
              new Date(b.created_at).getTime(),
          )
          // add the sorted list of pending bets to the end
          .concat(
            pendingBets.sort(
              (a, b) =>
                new Date(a.created_at).getTime() -
                new Date(b.created_at).getTime(),
            ),
          )
          // filter out bets from other sessions
          .filter((bet) => bet.session_id === sessionId),
      ),
      shareReplay(1),
    );

  readonly openBets: Signal<BetModel[] | null | undefined> = toSignal(
    this.openBets$,
  );

  async createBet(
    bet: OmitAutoGeneratedColumns<BetModel>,
  ): Promise<PostgrestSingleResponse<null>> {
    return this.supabase.from(Table.Bet).insert(bet);
  }

  async acceptBet(id: number): Promise<PostgrestSingleResponse<undefined>> {
    return this.supabase.rpc(Function.SubmitBetAccepted, { bet_id: id });
  }

  async rejectBet(id: number): Promise<PostgrestSingleResponse<BetModel[]>> {
    return this.supabase
      .from(Table.Bet)
      .update({ status: BetStatus.Rejected })
      .eq('id', id)
      .select();
  }
}
